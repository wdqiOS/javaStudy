package com.lz.operator;

public class MathDemo2 {

	public static void main(String[] args) {
		System.out.println("关系运算符");
		// >
		// 字符比较的是ASCII码值
		System.out.println('A' > 'B');
		int a = 3, b = 5;
		System.out.println("a<b="+(a<b));
		// <
		System.out.println("a>b="+(a>b));
		// <=
		System.out.println("a<=b="+(a<=b));
		// >=
		System.out.println("a>=b="+(a>=b));
		// !=
		System.out.println("a!=b="+(a!=b));
		
	}

}
