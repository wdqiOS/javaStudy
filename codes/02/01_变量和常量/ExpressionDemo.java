// 表达式
public class ExpressionDemo {
    public static void main(String[] args) {
        System.out.println(1 + 2 + 3);
        System.out.println("==========================");
        int result = 1 + 2 + 3;
        System.out.println(result);
        System.out.println("==========================");
        int a = 12;
        int b = 3;
        int c = 5;
        result = (a + b) / c + 10;
        System.out.println(result);
    }
}