// Boolean数据类型
public class BooleanDemo {
    public static void main(String[] args) {
        // boolean类型的常量：true/false
        System.out.println(true);
        System.out.println(false);
         // boolean类型的变量
         // 数据类型 变量名 = 该类型的常量值;
        // boolean isMan = 1; // 不兼容的类型: int无法转换为boolean
        boolean isMan = true;
        System.out.println(isMan);
        isMan = false;
        System.out.println(isMan);
    }
}
