class SwitchDemo2 {
    public static void main(String[] args) {
        char ch = 'c';
        switch(ch) {
            case 'A' : System.out.println('A'); break;
            case 'B' : System.out.println('B'); break;
            case 'C' : System.out.println('C'); break;
            default  : System.out.println('c'); 
        }
    }
}